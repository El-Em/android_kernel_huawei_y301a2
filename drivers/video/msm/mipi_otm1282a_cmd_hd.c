static int ch_used[3];

int mipi_otm1282a_device_register(struct msm_panel_info *pinfo,
					u32 channel, u32 panel)
{
	struct platform_device *pdev = NULL;
	int ret;
	if ((channel >= 3) || ch_used[channel])
		return -ENODEV;

	ch_used[channel] = TRUE;
	ret = mipi_otm1282a_lcd_init();
	if (ret) 
	{
		pr_err("mipi_otm1282a_lcd_init() failed with ret %u\n", ret);
		goto driver_failed;
	}
	
	pdev = platform_device_alloc("mipi_otm1282a", (panel << 8)|channel);
	if (!pdev)
	{
		ret = -ENOMEM;
		goto driver_failed;
	}

	otm1282a_panel_data.panel_info = *pinfo;
	ret = platform_device_add_data(pdev, &otm1282a_panel_data,sizeof(otm1282a_panel_data));
	if (ret) {
		printk(KERN_ERR
		  "%s: platform_device_add_data failed!\n", __func__);
		goto err_device_put;
	}
	ret = platform_device_add(pdev);
	if (ret) {
		printk(KERN_ERR
		  "%s: platform_device_register failed!\n", __func__);
		goto err_device_put;
	}
	return 0;

err_device_put:
	platform_device_put(pdev);

driver_failed:
	kfree(otm1282a_tx_buf.data);
	memset(&otm1282a_tx_buf,0,sizeof(struct dsi_buf));
	return ret;
}

