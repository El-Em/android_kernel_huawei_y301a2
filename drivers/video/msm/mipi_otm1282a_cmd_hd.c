int set_lcd_vs_power(int on)
{
	int rc = -1;
	if (gpio_is_valid(GPIO_LCD_BIAS_PM8038)) {
		gpio_set_value(GPIO_LCD_BIAS_PM8038,on);
		rc = 0;
	}
	else{
		printk("error: GPIO_LCD_BIAS_PM8038 = %d, rc=%d \n", on, rc);
	}

	return rc;
}

void set_lcd_power_reset(int on)
{
	int ret =0;
	static boolean gpio_st = false;

	if(gpio_st == false){
		ret = gpio_request(DISP_RST_GPIO, "disp_rst_n");
		if(ret)
		{
			printk("DISP_RST_GPIO request error!");
			return;
		}
		gpio_tlmm_config(GPIO_CFG(DISP_RST_GPIO, 0, GPIO_CFG_OUTPUT, GPIO_CFG_NO_PULL, GPIO_CFG_2MA), GPIO_CFG_ENABLE);
		gpio_st = true;
	}

	if(on)
	{
		gpio_direction_output(DISP_RST_GPIO, 1);
		mdelay(1);
		set_lcd_vs_power(LCD_VS_POWER_ON);
		mdelay(5);
		gpio_direction_output(DISP_RST_GPIO, 0);
		mdelay(1);
		gpio_direction_output(DISP_RST_GPIO, 1);
		mdelay(5);
		gpio_direction_output(DISP_RST_GPIO, 0);
		mdelay(1);
		gpio_direction_output(DISP_RST_GPIO, 1);
		msleep(50);
	}
	else
	{
		set_lcd_vs_power(LCD_VS_POWER_OFF);
		gpio_direction_output(DISP_RST_GPIO, 0);
	}
	return;
};
static int mipi_otm1282a_lcd_on(struct platform_device *pdev)
{
	static boolean first_called = true;
	boolean para_debug_flag = FALSE;
	uint32 para_num = 0;
	struct msm_fb_data_type *mfd;
	mfd = platform_get_drvdata(pdev);

	if (!mfd)
		return -ENODEV;
	if (mfd->key != MFD_KEY)
		return -EINVAL; 

	para_debug_flag = lcd_debug_malloc_get_para( "otm1282a_lcd_init_table_debug", 
			(void**)&otm1282a_lcd_init_table_debug,&para_num);

	if( (TRUE == para_debug_flag) && (NULL != otm1282a_lcd_init_table_debug))
	{
		process_mipi_table(mfd,&otm1282a_tx_buf,otm1282a_lcd_init_table_debug,
			 para_num, lcd_panel_hd);
	}
	else
	{
		if(first_called)
		{
			mipi_set_tx_power_mode(1);
			process_mipi_table(mfd,&otm1282a_tx_buf,otm1282a_hd_standby_exit_table,
				ARRAY_SIZE(otm1282a_hd_standby_exit_table), lcd_panel_hd);
			mipi_set_tx_power_mode(0);
	
			first_called = false;
		}
		else
		{
			set_lcd_power_reset(LCD_ON);
			mipi_dsi_clk_cfg(1);
			process_mipi_table(mfd,&otm1282a_tx_buf,otm1282a_lcd_init_table,
				init_table_len, lcd_panel_hd);
			mipi_dsi_clk_cfg(0);
		}
	}
	if((TRUE == para_debug_flag)&&(NULL != otm1282a_lcd_init_table_debug))
	{
		lcd_debug_free_para((void *)otm1282a_lcd_init_table_debug);
	}
	pr_info("leave mipi_otm1282a_lcd_on \n");

	return 0;
}

static int mipi_otm1282a_lcd_off(struct platform_device *pdev)
{
	struct msm_fb_data_type *mfd;
	mfd = platform_get_drvdata(pdev);

	if (!mfd)
		return -ENODEV;
	if (mfd->key != MFD_KEY)
		return -EINVAL;

	process_mipi_table(mfd,&otm1282a_tx_buf,otm1282a_hd_standby_enter_table,
		 ARRAY_SIZE(otm1282a_hd_standby_enter_table), lcd_panel_hd);
	set_lcd_power_reset(LCD_OFF);
	pr_info("leave mipi_otm1282a_lcd_off \n");
	return 0;
}

