void mipi_otm1282a_set_backlight(struct msm_fb_data_type *mfd)
{
	
	static int bl_level_flag = 0;
	if (wled_trigger_initialized) 
	{
		led_trigger_event(bkl_led_trigger, mfd->bl_level);
		//printk("In %s,level = %d\n",__func__,mfd->bl_level);
		return;
	}
	else
	{
		if( 0 == mfd->bl_level )
		{
			led_trigger_event(bkl_led_trigger, LED_OFF);
			bl_level_flag = 1;
		}
		else 
		{
			if(1 == bl_level_flag){
				led_trigger_event(bkl_led_trigger, LED_FULL);
				bl_level_flag = 0;
			}
		}
		otm1282a_hd_write_cabc_brightness_table[1].reg = mfd->bl_level; 
		process_mipi_table(mfd,&otm1282a_tx_buf,otm1282a_hd_write_cabc_brightness_table,
			 ARRAY_SIZE(otm1282a_hd_write_cabc_brightness_table), lcd_panel_hd);
		return;
	}
}


