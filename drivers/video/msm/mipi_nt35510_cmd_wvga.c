static char nt35510_manufacture_id[2] = {0x0A, 0x00}; /* DTYPE_DCS_READ */

static struct dsi_cmd_desc nt35510_manufacture_id_cmd = {
	DTYPE_DCS_READ, 1, 0, 1, 5, sizeof(nt35510_manufacture_id), nt35510_manufacture_id};

static void mipi_nt35510_manufacture_cb(char *data)
{
	char *lp = NULL;
	int i = 0;
	lp = data;
	/*read 2 byte back and print*/
	for(i = 0;i < 2;i ++)
	{
		if(lp != NULL)
		{
			printk("%s: register_value=0x%02x\n",__func__, *lp);
			lp ++;
		}
	}
}

static void mipi_nt35510_manufacture_id(void)
{
	struct dcs_cmd_req cmdreq;

	cmdreq.cmds = &nt35510_manufacture_id_cmd;
	cmdreq.cmds_cnt = 1;
	/*Qual Baseline Update,avoid backlight problem*/
	cmdreq.flags = CMD_REQ_RX | CMD_REQ_COMMIT | CMD_CLK_CTRL;
	cmdreq.rlen = 2;/*read 2 byte*/
	cmdreq.cb = NULL; 
	cmdreq.cb_hw =  mipi_nt35510_manufacture_cb;
	mipi_dsi_cmdlist_put(&cmdreq);
	/*
	 * blocked here, untill call back called
	 */
}

/*use qcom cmdlist interface in process_mipi_table()*/
static int mipi_nt35510_lcd_on(struct platform_device *pdev)
{
	/*use qcom cmdlist interface in process_mipi_table()*/
	boolean para_debug_flag = FALSE;
	uint32 para_num = 0;
	struct msm_fb_data_type *mfd;
	mfd = platform_get_drvdata(pdev);

	if (!mfd)
		return -ENODEV;
	if (mfd->key != MFD_KEY)
		return -EINVAL; 

	para_debug_flag = lcd_debug_malloc_get_para( "nt35510_lcd_init_table_debug", 
            (void**)&nt35510_lcd_init_table_debug,&para_num);

    if( (TRUE == para_debug_flag) && (NULL != nt35510_lcd_init_table_debug))
    {
        process_mipi_table(mfd,&nt35510_tx_buf,nt35510_lcd_init_table_debug,
		     para_num, lcd_panel_wvga);
    }
	else
	{
		mipi_set_tx_power_mode(1);
		process_mipi_table(mfd,&nt35510_tx_buf,nt35510_wvga_standby_exit_table,
		 	ARRAY_SIZE(nt35510_wvga_standby_exit_table), lcd_panel_wvga);
		mipi_set_tx_power_mode(0);
	}

	if((TRUE == para_debug_flag)&&(NULL != nt35510_lcd_init_table_debug))
	{
		lcd_debug_free_para((void *)nt35510_lcd_init_table_debug);
	}
	/* clean up ack_err_status by read 0x0A*/
	mipi_nt35510_manufacture_id();
	pr_info("leave mipi_nt35510_lcd_on \n");

	return 0;
}

static int mipi_nt35510_lcd_off(struct platform_device *pdev)
{
	/*use qcom cmdlist interface in process_mipi_table()*/
	struct msm_fb_data_type *mfd;
	mfd = platform_get_drvdata(pdev);

	if (!mfd)
		return -ENODEV;
	if (mfd->key != MFD_KEY)
		return -EINVAL;

	process_mipi_table(mfd,&nt35510_tx_buf,nt35510_wvga_standby_enter_table,
		 ARRAY_SIZE(nt35510_wvga_standby_enter_table), lcd_panel_wvga);
	pr_info("leave mipi_nt35510_lcd_off \n");
	return 0;
}

