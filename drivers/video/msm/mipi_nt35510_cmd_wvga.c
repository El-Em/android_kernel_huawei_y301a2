/*move one line before*/
void mipi_nt35510_set_backlight(struct msm_fb_data_type *mfd)
{

	static int bl_level_flag = 0;
	/*qcom cmdlist interface can prevent dead lock,so delete some invalid operation*/
	if (wled_trigger_initialized) 
	{
		led_trigger_event(bkl_led_trigger, mfd->bl_level);
		//printk("In %s,level = %d\n",__func__,mfd->bl_level);
		return;
	}
	else
	{
		if( 0 == mfd->bl_level )
		{
			led_trigger_event(bkl_led_trigger, LED_OFF);
			bl_level_flag = 1;
		}
		else 
		{
			if(1 == bl_level_flag){
				led_trigger_event(bkl_led_trigger, LED_FULL);
				bl_level_flag = 0;
			}
		}
		nt35510_wvga_write_cabc_brightness_table[1].reg = mfd->bl_level; 
		process_mipi_table(mfd,&nt35510_tx_buf,nt35510_wvga_write_cabc_brightness_table,
			 ARRAY_SIZE(nt35510_wvga_write_cabc_brightness_table), lcd_panel_wvga);
		return;
	}
}

