static int32_t s5k9a1_write_prev_exp_gain(struct msm_sensor_ctrl_t *s_ctrl, uint16_t gain, uint32_t line)
{
    int32_t rc = 0;
    uint32_t fl_lines, offset;

    S5K9A1_DBG_FLOW("s5k4e1_write_prev_exp_gain :%d %d\n", gain, line);

    if(NULL == s_ctrl)
    {
        S5K9A1_DBG_ERR("[%s], Null para\n", __func__);
        return -EINVAL;
    }


    fl_lines = s_ctrl->curr_frame_length_lines;
    
    offset = 8;

    fl_lines = (fl_lines * s_ctrl->fps_divider) / Q10;

    S5K9A1_DBG_PARA("current frame_l_line = [%d], offset = [%d], fl_line = [%d] \n", 
            s_ctrl->curr_frame_length_lines, offset, fl_lines);

    /* Enable system clock unit mode for Fps and Shutter contorl */
    msm_camera_i2c_write_tbl(s_ctrl->sensor_i2c_client,
                             s5k9a1_default_enable_config,
                             ARRAY_SIZE(s5k9a1_default_enable_config),
                             MSM_CAMERA_I2C_WORD_DATA);

	/* Analogue Gain */
    s5k9a1_sensor_write_gain_control(s_ctrl, gain);

    /* if exposure line is longer  */
    if (line > (s_ctrl->curr_frame_length_lines - offset)) {
        fl_lines = line + offset;
    } else if (line < (s_ctrl->curr_frame_length_lines - offset)) {
        /* if exposure line is shorter  */
        fl_lines = line ;//+ offset;
        if (fl_lines < s_ctrl->curr_frame_length_lines)
            fl_lines = s_ctrl->curr_frame_length_lines;
    }

    /* write frame_length_line */
    s5k9a1_sensor_write_frame_length(s_ctrl,fl_lines);
    s5k9a1_sensor_write_line_length(s_ctrl);
    /* Coarse Integration Time */
    s5k9a1_sensor_write_coarse_integration(s_ctrl,line);

    return rc;
}


