int32_t s5k9a1_sensor_i2c_probe(struct i2c_client *client,
	const struct i2c_device_id *id)
{
	int rc = 0;
	struct msm_camera_sensor_info *s_info = NULL;

    S5K9A1_DBG_FLOW("[%s], [%d]\n", __func__, __LINE__);

	rc = msm_sensor_i2c_probe(client, id);

	s_info = client->dev.platform_data;
	if (s_info == NULL) {
		S5K9A1_DBG_ERR("%s %s NULL sensor data\n", __func__, client->name);
		return -EFAULT;
	}
	S5K9A1_DBG_PARA("%s: sensor name =%s\n", __func__, s_info->sensor_name);
	return rc;
}

